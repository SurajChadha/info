public class Employee
{
	String name;
	public String id;
	private double salary;
	public Employee(string name, string id,double salary)
	{
		this .name = name;
		this.id = id;
		this.salary = salary;
	}
	public string getName()
	{
		return name;
	}
	public string getId()
	{
		return id;
	}
	public double getSalary()
	{
		return salary;
	}
}
	